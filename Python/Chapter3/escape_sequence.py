# Common Escape Sequences:


# Escape	Meaning	Example Output
# \n	Newline	Line 1
# Line 2
# \t	Horizontal Tab	Word1 Word2
# \\	Backslash	\
# \'	Single Quote	'
# \"	Double Quote	"
# \r	Carriage Return	Resets to start of line (used with \n)
# \b	Backspace	Deletes previous char (visible in some terminals)
# \f	Form feed	Page break (rarely used)
# \a	Bell (alert)	Makes a sound in some terminals
# \v	Vertical Tab	Rarely used
# \ooo	Character with octal value	\101 → A
# \xhh	Character with hex value	\x41 → A